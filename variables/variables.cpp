#include <iostream>
using namespace std;

int main() {
    // دروووود! اینجا میخوایم با متغیرها آشنا شیم
    // متغیر یعنی یه جعبه تو حافظه که می‌تونی توش اطلاعات ذخیره کنی
    // مثلاً عدد، متن، و غیره

    int age = 25;      // عدد صحیح
    double weight = 68.5; // عدد اعشاری
    char grade = 'A';  // یه کاراکتر
    string name = "Ali"; // یه رشته (متن)

    cout << "Esme man " << name << " hast." << endl;
    cout << "Senem " << age << " sale." << endl;
    cout << "Vaznam " << weight << " kg hast." << endl;
    cout << "Nemtan " << grade << " hast." << endl;

    return 0;
}
